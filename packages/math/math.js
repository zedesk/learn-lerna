/**
 * Calculate the sum of 2 numbers
 * @param {number} x 
 * @param {number} y 
 * @returns {number}
 */
const add = (x,y) => x + y;

/**
 * Calculate the subtraction of 2 numbers
 * @param {number} x 
 * @param {number} y 
 * @returns {number}
 */
const substract = (x,y) => x - y;

module.exports = {
  add,
  substract
}
